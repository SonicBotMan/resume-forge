EXTRACT_PROMPT = """你是一个资深的简历信息提取专家。你的任务是从职业材料中提取所有可用于简历的信息。

材料可能包含：简历、述职报告、周报、OKR汇报、项目总结、工作汇报等。

请提取以下所有维度的信息（如果某个维度在材料中找不到，返回空数组/空对象）：

1. **个人基本信息**：姓名、联系方式（手机、邮箱）、所在城市
2. **工作经历**：公司名称、职位/职级、部门、在职时间段。注意区分不同的任职经历。
3. **项目经历**：项目名称、角色、时间范围、背景/目标、具体行动、成果（尽量量化）
4. **教育背景**：学校、专业、学历、时间段
5. **技能**：技术技能、工具、方法论、软技能
6. **成果与亮点**：任何量化数据、获奖、荣誉、关键指标

 材料内容：
---
<user_content>
{chunk_text}
</user_content>
---

请以JSON格式输出：
{{
    "personal_info": {{
        "name": "姓名（如果提到）",
        "phone": "手机号",
        "email": "邮箱",
        "location": "所在城市"
    }},
    "work_experience": [
        {{
            "company": "公司名称",
            "position": "职位/职级",
            "department": "部门",
            "period": "时间段，如 2020.03-至今",
            "highlights": ["该职位下的核心职责或成果，1-3条"]
        }}
    ],
    "projects": [
        {{
            "name": "项目名称",
            "role": "角色",
            "time_range": {{"start": "开始月份", "end": "结束月份"}},
            "situation": "背景/目标",
            "task": "任务描述",
            "action": "具体行动",
            "result": "成果（包含量化数据）"
        }}
    ],
    "education": [
        {{
            "school": "学校名称",
            "major": "专业",
            "degree": "学历（本科/硕士/博士）",
            "period": "时间段"
        }}
    ],
    "skills_mentioned": ["技能/工具/方法论名称"],
    "tools_mentioned": ["具体工具/平台名称"],
    "achievements": ["量化成果或荣誉，如：DAU达到10万+"]
}}

注意：
- 工作经历和项目经历是不同的：工作经历是「在某公司某职位做了什么」，项目经历是「某个具体项目」
- 从述职材料中，如果提到「负责XX业务」「在XX部门」，这属于工作经历
- 职级信息（如P7、高级产品经理）要提取到position字段
- 时间信息非常重要：任何提到年份、月份的地方都要提取
- 如果材料中提到多个公司或多个阶段，每个都要单独提取
- 不要编造信息，只提取材料中明确提到的内容
"""


def get_extract_prompt(chunk_text: str) -> str:
    return EXTRACT_PROMPT.format(chunk_text=chunk_text)
