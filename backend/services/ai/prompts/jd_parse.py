"""Enhanced JD parsing prompt for structured job description extraction."""

JD_PARSE_PROMPT = """你是一位资深的招聘分析专家，擅长从职位描述（JD）中精确提取结构化信息，并为候选人提供深度的求职策略洞察。

请仔细分析以下职位描述，提取所有关键要素，并给出专业的人才画像和面试策略建议。

 ## 职位描述
---
<user_content>
{jd_text}
</user_content>
---

## 提取要求

### 1. 技能分类
- **required_skills**: 明确标注为"必须""要求""必备"的硬技能（编程语言、框架、工具、证书等）。技能名称统一使用行业通用名称（如 "Python" 而非 "python"，"Kubernetes" 而非 "k8s"）。
- **preferred_skills**: 标注为"优先""加分""了解""熟悉"的技能。这些是候选人可以通过展示来增加竞争力的非必须技能。
- **soft_skills**: 沟通能力、团队协作、领导力等软性要求。仔细识别隐性软技能要求（如"跨部门协调"暗示沟通能力，"带领小团队"暗示管理能力）。

### 2. 职位层级
判断 seniority_level:
- "junior": 应届/1-2年经验，关键词如"初级""助理""实习生"
- "mid": 3-5年经验，无特殊层级标注，关键词如"中级""独立负责"
- "senior": 5-8年经验，关键词如"高级""资深""主导"
- "staff": 8年以上，关键词如"专家""架构师""总监""VP"

### 3. 基础维度
- **education_requirements**: 学历要求（如"本科以上""硕士优先""985/211优先"）。如未明确提及，根据岗位层级和行业惯例推断。
- **experience_years**: 工作年限要求（如"3-5年""5年以上"）。注意区分"相关经验"和"总工作年限"。
- **industry_keywords**: 行业领域关键词（如"金融科技""电商""SaaS""医疗健康""新能源汽车"）。识别JD中提及的具体行业背景和业务领域。
- **role_summary**: 一句话概括岗位核心职责。应包含：做什么（技术方向/业务领域）、怎么做（方法论/工具）、达到什么目标（产出/影响）。
- **hiring_motivation**: 推断招聘动因。常见类型："业务扩张"（新业务线/新市场）、"团队组建"（从零搭建）、"技术升级"（技术栈迁移/架构优化）、"人才替换"（核心岗位流失）、"战略转型"（业务方向调整）。

### 4. 人才侧重（talent_focus）
分析这个岗位最看重候选人哪方面的能力特质，归入以下类别之一：
- **"技术深度型"**: 岗位核心是解决复杂技术问题，要求深入的技术栈掌握，如底层开发、算法优化、性能调优。示例：高级后端工程师、算法工程师、数据库内核开发。
- **"业务驱动型"**: 岗位核心是推动业务结果，需要强业务理解和数据驱动决策，如增长、运营分析、业务系统开发。示例：产品经理、业务分析师、商业化开发。
- **"管理复合型"**: 岗位要求带团队或跨团队协调，技术+管理双重要求。示例：技术Leader、工程经理、Tech Lead。
- **"创新探索型"**: 岗位核心是探索新技术或新方向，容错空间大，需要前沿技术敏感度。示例：AI研究员、创新实验室工程师、预研架构师。
- **"执行落地型"**: 岗位核心是高效交付，注重工程规范、流程优化、稳定性保障。示例：SRE、DevOps工程师、质量工程师。
- **"综合型"**: 以上多项均衡，无特别突出的单一维度。

输出格式：先给出分类标签，然后用简短说明解释为什么归入该类别。例如：
"技术深度型 — 该岗位要求深入掌握分布式系统底层原理，核心职责是解决高并发架构难题"

### 5. 特殊门槛（special_barriers）
识别JD中可能成为"硬性筛选条件"的特殊要求，这些是候选人容易忽视但HR/面试官会严格卡的条件。示例：
- 特定证书/资质："PMP证书""CPA""AWS Solutions Architect认证""CISP"
- 安全审查："通过背景调查""持有涉密资质""政审"
- 地域/差旅："接受50%出差""需驻场""接受外派东南亚"
- 语言要求："英语可作为工作语言""日语N1""需要与海外团队日常沟通"
- 行业经验："5年以上金融行业经验""医疗器械法规经验"
- 年龄/学历硬卡："35岁以下""仅限985/211""博士学历"
- 特定领域知识："对支付清算体系有深刻理解""熟悉FDA审批流程"

只列出真正的门槛条件，不要列出通用要求（如"良好的沟通能力"不算门槛）。如果JD中无明显门槛，返回空列表。

### 6. 发展路径（career_growth）
描述候选人在这个岗位工作2-3年后的典型职业发展方向。考虑：
- 技术路线：从该岗位能积累什么核心技术深度，下一步可晋升到什么级别
- 管理路线：是否有带团队机会，能否转向管理方向
- 行业价值：该岗位经验在行业内是否稀缺，跳槽价值如何
- 技能增值：能获得哪些市场上高价值的复合技能

示例输出：
"2-3年后可成长为分布式系统架构师，具备大规模系统设计经验，在互联网大厂有较高竞争力；也可向技术管理方向转型，负责基础设施团队"

### 7. 面试侧重（interview_focus）
列出3-5个面试过程最可能重点考察的能力维度。根据岗位类型和技术要求推断：
- "系统设计能力" — 需要设计复杂系统或架构
- "算法与数据结构" — 有明确算法考核要求
- "业务Sense" — 需要理解和推动业务
- "团队管理" — 有带人职责
- "技术深度" — 需要底层原理掌握
- "项目经验深挖" — 注重实战经历
- "抗压能力" — 高强度/On-call相关
- "沟通表达" — 跨部门协作要求高
- "代码质量意识" — 注重工程规范
- "问题解决能力" — 故障排查/性能优化场景

### 8. 薪资洞察（salary_insight）
根据岗位的职级、技术栈、行业和地域，给出薪资分析：
- 估计薪资范围（月薪或年薪，标注"约"字表示估算）
- 与市场平均水平对比（高于/低于/持平）
- 影响薪资的关键因素（技术稀缺性、行业溢价、公司阶段等）

示例输出：
"预计年薪40-60万，处于市场中位偏上。Go语言+分布式存储的技能组合较稀缺，有15-20%技术溢价空间。创业公司阶段股权激励可能有额外回报"

## 输出格式

请严格返回以下JSON结构（不要包含任何其他内容，不要用markdown代码块包裹）：
{{
    "required_skills": ["技能1", "技能2"],
    "preferred_skills": ["技能1", "技能2"],
    "soft_skills": ["技能1", "技能2"],
    "seniority_level": "mid",
    "education_requirements": "学历要求",
    "experience_years": "年限要求",
    "industry_keywords": ["行业1", "行业2"],
    "role_summary": "岗位核心职责概括",
    "hiring_motivation": "招聘动因",
    "talent_focus": "人才侧重类型和说明",
    "special_barriers": ["门槛1", "门槛2"],
    "career_growth": "发展路径说明",
    "interview_focus": ["面试侧重1", "面试侧重2"],
    "salary_insight": "薪资洞察说明"
}}

## 重要提示
- 技能名称统一使用行业通用名称（如 "Python" 而非 "python"）
- 如果某字段在JD中未明确提及，根据上下文和行业常识合理推断
- 确保每个字段都有值，不要返回空字符串或空列表（special_barriers除外，无门槛时可为空列表）
- talent_focus 必须以六种类型之一开头："技术深度型"、"业务驱动型"、"管理复合型"、"创新探索型"、"执行落地型"或"综合型"
- interview_focus 必须包含3-5个具体维度
- salary_insight 如无法准确判断，给出合理区间并标注"供参考"
"""


def get_jd_parse_prompt(jd_text: str) -> str:
    """Generate JD parsing prompt.

    Args:
        jd_text: Raw job description text.

    Returns:
        Formatted prompt string.
    """
    return JD_PARSE_PROMPT.format(jd_text=jd_text)
